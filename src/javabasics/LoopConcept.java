package javabasics;

public class LoopConcept {

	public static void main(String[] args) {

		// While Loop

		int i = 1;

		while (i <= 10) {
			System.out.println(i);
			i = i + 1;

		}
		// for loop

		for (int j = 0; j <= 100; j++) {

			System.out.println(j);
		}

		for (int x = 10; x >= 0; x--) {

			System.out.println(x);

		}

	}

}
